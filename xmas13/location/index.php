<h2>Getting to Canberra</h2>

<h3>From Sydney:</h3>
<ul>
	<li><a href="http://www.virginaustralia.com/au/en/">Virgin</a> and <a href="http://www.qantas.com.au/travel/airlines/home/au/en">Qantas</a> both fly to Canberra airport. Bus and taxi services operate between the airport and the centre of town.</li>
	<li>Murrays operate an <a href="http://www.murrays.com.au/ExpressService.aspx">express bus from Sydney to the Jolimont Centre</a> in Civic. It is a short walk to the centre of town. ANU campus is a 20 minute walk away.</li>
	<li>The <a href="http://www.nswtrainlink.info/destinations/network">train from Sydney</a> stops in Kingston, which is a 15 minute drive from the centre of town.</li>
	<li>Travelling to Canberra by car takes approximately three hours on the Federal Highway.</li>
</ul>

<h3>From everywhere else:</h3>
<ul>
	<li><a href="http://www.virginaustralia.com/au/en/">Virgin</a> and <a href="http://www.qantas.com.au/travel/airlines/home/au/en">Qantas</a> both fly to Canberra airport.</li>
</ul>

<h2>Getting to Mt Stromlo</h2>
<ul>
	<li><b>Please note there is no public transport to Mt Stromlo Observatory from town.</b> You can get to the observatory by car or taxi. For those without a car, we will organise transport for all participants from the centre of town, either on the student minibus or a lift with someone.</li>
</ul>

